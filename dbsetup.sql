CREATE TABLE Posts (
	postId INT(11) NOT NULL AUTO_INCREMENT,
    postname VARCHAR(45) DEFAULT NULL,
    description VARCHAR(200) DEFAULT NULL,
    createdBy VARCHAR(11) DEFAULT NULL,
    PRIMARY KEY(postId)
)ENGINE=InnoDB AUTO_INCREMENT=0;

