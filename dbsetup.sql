CREATE TABLE Users (
	userId INT(11) NOT NULL AUTO_INCREMENT,
    name VARCHAR(45) DEFAULT NULL,
    email VARCHAR(45) DEFAULT NULL,
    password VARCHAR(150) DEFAULT NULL,
    PRIMARY KEY(UserId)
) ENGINE=InnoDB AUTO_INCREMENT=0;

